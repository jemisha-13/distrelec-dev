package com.namics.distrelec.b2b.core.mail.internal.data;

public class MissingImageMediaFileData {

    private String mediaFolder;
    private String mediaPath;
    private String mediaLocation;
    private String mediaName;

    public String getMediaFolder() {
        return mediaFolder;
    }

    public void setMediaFolder(final String mediaFolder) {
        this.mediaFolder = mediaFolder;
    }

    public String getMediaPath() {
        return mediaPath;
    }

    public void setMediaPath(final String mediaPath) {
        this.mediaPath = mediaPath;
    }

    public String getMediaLocation() {
        return mediaLocation;
    }

    public void setMediaLocation(final String mediaLocation) {
        this.mediaLocation = mediaLocation;
    }

    public String getMediaName() {
        return mediaName;
    }

    public void setMediaName(final String mediaName) {
        this.mediaName = mediaName;
    }
}
